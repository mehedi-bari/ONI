            <p style="text-align: center;" style="border-width: all;" style="background-color: grey;">Setsubun (節分) is the day before the beginning of spring in the old calendar in Japan. The name literally means 'seasonal division', referring to the day just before the first day of spring in the traditional calendar, known as Setsubun; though previously referring to a wider range of possible dates, Setsubun is now typically held on February 3 (in 2021 it was on 2nd February), with the day after – the first day of spring in the old calendar – known as Risshun (立春). Both Setsubun and Risshun are celebrated yearly as part of the Spring Festival (Haru matsuri (春祭)) in Japan. In its association with the Lunar New Year, Setsubun, though not the official New Year, was thought of as similar in its ritual and cultural associations of 'cleansing' the previous year as the beginning of the new season of spring. Setsubun was accompanied by a number of rituals and traditions held at various levels to drive away the previous year's bad fortunes and evil spirits for the year to come.</p>
